import os

BASE_URL = os.environ.get('REACT_APP_BACKEND_URL', 'https://mockup-manager.preview.emergentagent.com')


class TestProductBoxAPIs:
    """Test suite for Product Box APIs"""
    
    def test_products_endpoint_returns_data(self):
        """Test GET /api/product-box/products returns products"""
        response = requests.get(f"{BASE_URL}/api/product-box/products?limit=10")
        assert response.status_code == 200, f"Products endpoint failed: {response.text}"
        
        data = response.json()
        assert "products" in data, "Response should have 'products' field"
        assert "total" in data, "Response should have 'total' field"
        assert len(data["products"]) > 0, "Should return at least one product"
        assert data["total"] >= 2500, f"Expected 2500+ products, got {data['total']}"
        
        # Validate product structure
        product = data["products"][0]
        assert "id" in product, "Product should have 'id' field"
        assert "name" in product, "Product should have 'name' field"
        print(f"✅ Products endpoint returned {data['total']} total products")
    
    def test_huft_products_search_returns_20(self):
        """Test HUFT products search returns exactly 20 products"""
        response = requests.get(f"{BASE_URL}/api/product-box/products?search=HUFT&limit=25")
        assert response.status_code == 200, f"HUFT search failed: {response.text}"
        
        data = response.json()
        assert data["total"] == 20, f"Expected 20 HUFT products, got {data['total']}"
        
        # Verify all products are HUFT brand
        for product in data["products"]:
            assert "HUFT" in product.get("name", ""), f"Product {product.get('id')} is not a HUFT product"
        
        print(f"✅ HUFT products search returned {data['total']} products")
    
    def test_product_stats_total_count(self):
        """Test product stats API returns 2503+ total products"""
        response = requests.get(f"{BASE_URL}/api/product-box/stats")
        assert response.status_code == 200, f"Stats endpoint failed: {response.text}"
        
        data = response.json()
        assert "total" in data, "Stats should have 'total' field"
        assert data["total"] >= 2503, f"Expected 2503+ total, got {data['total']}"
        
        # Validate stats structure
        assert "by_status" in data, "Stats should have 'by_status'"
        assert "by_pillar" in data, "Stats should have 'by_pillar'"
        assert "mira" in data, "Stats should have 'mira' section"
        
        print(f"✅ Stats returned total: {data['total']}, active: {data['by_status'].get('active', 0)}")
    
