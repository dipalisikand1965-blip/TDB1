    def test_product_structure_has_breed_metadata(self):
        """Test that products have breed_metadata field"""
        # Get Labrador Pawfect Bandana specifically
        response = requests.get(f"{BASE_URL}/api/product-box/products?search=Labrador%20Pawfect%20Bandana&limit=5")
        assert response.status_code == 200
        
        data = response.json()
        if data["total"] > 0:
            product = data["products"][0]
            assert "breed_metadata" in product, "Product should have breed_metadata field"
            
            breed_meta = product.get("breed_metadata", {})
            if breed_meta.get("breed_specific"):
                assert "breeds" in breed_meta, "breed_metadata should have 'breeds' field"
                assert "Labrador" in breed_meta.get("breeds", []), "Should have Labrador in breeds"
            print(f"✅ Product has correct breed_metadata: {breed_meta}")
        else:
            print("⚠️ No Labrador Pawfect Bandana found to test breed_metadata")


