    def test_search_breed_specific_products(self):
        """Test searching for breed-specific personalized products"""
        # Search for Labrador Pawfect Bandana (should be generated by breed generator)
        response = requests.get(f"{BASE_URL}/api/product-box/products?search=Labrador%20Pawfect&limit=10")
        assert response.status_code == 200, f"Breed-specific search failed: {response.text}"
        
        data = response.json()
        assert data["total"] > 0, "Should find Labrador Pawfect products"
        
        # Verify breed_metadata has correct breed
        found_breed_specific = False
        for product in data["products"]:
            breed_metadata = product.get("breed_metadata", {})
            if breed_metadata.get("breed_specific") and "Labrador" in breed_metadata.get("breeds", []):
                found_breed_specific = True
                break
        
        assert found_breed_specific, "Should find breed-specific Labrador product with correct breed_metadata"
        print(f"✅ Found breed-specific Labrador products")
    
